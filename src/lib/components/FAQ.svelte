	<h3 class="sr-only">Frequently Asked Questions</h3>
	<dl>
		<dt class="mt-8 font-medium text-gray-900">What grind level should I choose?</dt>
		<dd class="prose prose-sm mt-2 max-w-none text-gray-500">
			<p>If you have or are able to purchase a coffee grinder, we recommend purchasing whole bean coffee. 
				Coffee will lose its freshness much more quickly after it has been ground. 
				That said, convenience is also an important part of enjoying your favorite brew. For that reason, we offer a variety of ground coffee options.
				When selecting ground coffee, keep in mind the way you intend to brew it. These are general rules of thumb:</p>  
				<ul>
					<li>For a drip coffee maker with a flat-bottomed basket (MOST COMMON), select a medium grind.</li>
					<li>For a drip coffee maker with a cone-shaped filter, select a fine grind.</li>
					<li>For an aeropress, select a fine grind.</li>
					<li>For a French press, select a coarse grind.</li>
					<li>For cold brew, select a coarse grind.</li>
				</ul>
		</dd>
		<dt class="mt-8 font-medium text-gray-900">How long will my coffee last?</dt>
		<dd class="prose prose-sm mt-2 max-w-none text-gray-500">
			<p>Coffee is a considered to be a shelf-stable food that is safe to consume for many months after it has been roasted. 
				That said, just because a food is safe, that does not mean it will have optimal taste. 
				Coffee reaches optimal flavor about 2-3 days after roasting, and then it begins to lose that flavor at a decreasing rate. 
				We recommend choosing a size that will last you one month or less if possible. Life is too short to drink stale coffee!</p>
		</dd>
		<dt class="mt-8 font-medium text-gray-900">How can I tell when my coffee was roasted?</dt>
		<dd class="prose prose-sm mt-2 max-w-none text-gray-500">
			<p>Our coffee is shipped on the same day it is roasted, with the exception of K-Cups, office packs, and our small 2 oz sample packs. 
				Why?  After roasting, coffee releases carbon dioxide, which can cause the coffee to expand and burst the packaging.  
				Our K-Cups, office packs, and 2 oz sample packs do not have the little valves that are on our 10 oz, 16 oz, and 5 lb packaging that prevent the bags from bursting.  
				As a result, the coffee has to "rest" for 3 days after roasting before it is safe to package and ship.  
				This is inherent to the design of K-Cups and is not specific to our processes. 
				We make runs of K-Cups and office packs about once per week.
			</p>
		</dd>
	</dl>
